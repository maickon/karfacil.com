	<h1>A karfacil.com</h1>
	<p>A karfacil.com é uma empresa de divulgação de veículos que visa o atendimento das lojas de Campos e região.
	Catalogamos os veículos de nossas lojas filiadas para facilitar a vida de nossos clientes, permitindo lhes uma pré seleção
	de seu veículo desejado antes de visitar uma loja.</p>
	<p>Também trabalhamos com propagandas de outros tipos de estabelecimento como por exemplo lojas de autopeças, lojas que 
	trabalham com concerto de veículos, postos de gasolina e etc. Tudo isso para que o cliente encontre tudo que ele precise
	 em um só lugar.</p>

	<h1>Motivação</h1>
	<p>Percebemos que dentro da cidade de Campos dos Goytacazes, as lojas que oferecem serviços de compra venda e troca de 
	veículos, não possuíam uma apresentação web. Partindo desse ponto decidimos construir um sistema que unisse
	todas as lojas da cidade em um só lugar para que seus clientes pudessem fazer sua pesquisa de forma mais rápida no momento
	em que quisessem adquirir um veículo através de algumas dessas lojas.</p>
	<p>Foi daí que surgiu a idéia da karfacil.com que seria rede de comunicação focada exclusivamente na divulgação de propagandas e serviços.
	</p>
	<h1>Nossa visão</h1>
	<p>Ser uma empresa de grande porte que atenda todas as necessidades do universo automotivo aos nossos clientes.
	Pretendemos expandir o nosso negócio em todo o Brasil, colocando uma filial em cada estado.</p>
	
	<h1>Nossa Missão</h1>
	<p>Divulgação de compra, venda e troca veículos. Ampliamos o reconhecimento de nossas lojas filiadas por meio de anúncios 
	em rádios, Outdoor, cartões de apresentação, eventos e etc. Atraindo o cliente ao nosso site para conhecer as nossas lojas filiadas.
	<p>Também focamos nosso relacionamento com outros tipos de estabelecimento, como lojas de autopeças, auto escolas, borracharias,
	postos de gasolina, enfim queremos unir toda informação relacionada a veículos em um só lugar, queremos que nossos clientes
	encontrem tudo o que precisa em nosso website da melhor maneira possível.</p>
	
	<h1>Nossos valores</h1>
	<p>Seriedade, qualidade, honestidade e competência. </p>
	<p>Dentro do que nós da Karfacil acreditamos, uma empresa que falte com um dos valores citado acima, não deve ser
	chamado de empresa.</p>
