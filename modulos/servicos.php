<div align="center">
	<h1>Nossos serviços</h1>
	<p>Nesta página vamos explicar como que funciona os nossos serviçõs de cadastramento. 
	Através de uma ligação você estará investindo na divulgação de seu estabelecimento.</p>
	<h1>As lojas de veículos</h1>
	<p>Uma vez feita a ligação, estaremos combinando o dia e a hora em que visetaremos o seu estabelecimento
	para o cadastramento de seus veículos em nosso sistema. Este processo consiste em 3 passos:</p>
	<p>
	<ul>
		<li>Passo 1-Cadastramento do dono da loja</li>
		<li>Passo 2-Cadastramento da loja</li>
		<li>Passo 3-Cadastramento dos veículos</li>
	</ul>
	<p>
	No passo 1, precisamos desse cadastro para que o sistema saiba a quem a loja pertençe, 
	além disso fica mais fácil de encontrar os dados cadastrais do dono pelo fato de estar bem organizado no sistema. 
	</p>
	<p>No passo 2, estaremos cadastrando a loja pois a loja terá uma página de apresentação e nesta página,
	o cliente encontrará a logomarca da loja, logo abaixo uma descrição do endereço de onde o estabelecimento se 
	encontra. E mais abaixo ainda uma foto do estabelecimento que será tirada no dia do cadastramento dos veículos. </p>
		
	<p>No passo 3, estaremos cadastrandos os veículos na devida loja cadastrada em nosso sistema.</p>
	
	<p>Ambas as imagens apresentadas nesta página, são links que quando clicados levarão o cliente para página
	de visualização dos veículos da loja.</p>
	
	<p>A página de visualização de veículo, consiste num slide que apresenta intercaladamente cada uma das imagens
	do veículo cadastrado. Abaixo se encontra a descrição do veículo que é fornecida pela loja responsável, 
	o endereço e dados para contato. Mais abaixo ainda em vermelho, um trecho visando ao cliente que diz que 
	todas as informações presentes ali são de inteira responsabilidade da loja responsável.</p>
	
	<h1>As propagandas</h1>
	<p>O cadastro de propagandas é bem mais simples do que o cadastro de lojas de veículos, pois seu cadastro
	compreende em 2 passos.</p>
	<ul>
		<li>Passo 1-Cadastramento do dono de propaganda</li>
		<li>Passo 2-Cadastramento da propaganda em si</li>
	</ul>
	
	<p>No passo 1, vamos cadastrar os dados do dono do estabelecimento. Este passo é muito parecido com o cadastro de 
	dono de loja de veículos.</p>
	<p>No passo 2 cadastraremos a propaganda. A propaganda cadastrada, terá direito a 4 imagens que serão apresentadas
	dentro de um slide. Neste slide as imagens ficarão se intercalando, permitindo que o cliente visualize todos os 
	4 slides.</p>
	<p>Mais abixo o endereço do estabelecimento e uma descrição que pode abordar o assunto que o dono desejar.</p>
	<p>
	<h1>Veículos exclusivos</h1>
	<p>Nesta parte tratamos de clientes que desejam se desfazer de seus veículos por si só. Então para facilitar sua vida,
	cadastramos o seu veículo em nosso sistema e colocamos os dados do dono para contato. Dessa forma ele poderá encontrar
	um comprador por intermédio da karfacil.com. Apenas 2 passos são necessários para que esse cadastro seja possivel.</p>
	<ul>
		<li>Passo 1-Cadastramento do dono do veículo</li>
		<li>Passo 2-Cadastramento do veículo em si</li>
	</ul>
	<p>No passo 1, vamos cadastrar o nosso cliente no sistema.</p>
	<p>No passo 2, quando ja temos um cliente cadastrado, cadastramos o veículo desejado associado ao seu respectivo dono.</p>
	<p>A página de veículos exclusivos consiste na apresentação dos dados para contato do cliente cadastrado e logo
	abaixo dentro de um slide, temos as 4 imagens de que o cliente tem direito para a visualização do veículo.
	As imagens passam intercaladamente permitindo que seja visto todas as imagens.</p>
